function AHARateCalculator( age_in )
{
	this.age = age_in;
	this.maximumHeartRate = 220 - this.age;
	this.minimumRecommended = 0.5 * this.maximumHeartRate;
	this.maximumRecommended = 0.85 * this.maximumHeartRate;

	this.getMinimumRecommended = getMinimumRecommended;
	this.getMaximumRecommended = getMaximumRecommended;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
function getMinimumRecommended()
{
	return this.minimumRecommended;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
function getMaximumRecommended()
{
	return this.getMaximumRecommended;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
function UpdateHR()
{
	var age = parseFloat( document.theForm.age.value );
	var heartRateCalculator = new AHARateCalculator( age );
	var min = heartRateCalculator.getMinimumRecommended();
	var max = heartRateCalculator.getMaximumRecommended();
	document.theForm.min.innerHTLM = min.toString();
	document.theForm.max.innerHTLM = max.toString();
}